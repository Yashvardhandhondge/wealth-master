function setLanguage(lang) {
    // Set HTML lang attribute
    document.documentElement.lang = lang;

    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Use innerHTML to render the <strong> tags in some translations
            el.innerHTML = translations[lang][key];
        }
    });

    // Update the nice-select dropdown display
    const switcher = document.getElementById('languageSwitcher');
    if (switcher && switcher.niceSelect) {
        switcher.value = lang;
        switcher.niceSelect.update();
    }
}

// Function to get language from URL or browser
function getInitialLanguage() {
    const urlParams = new URLSearchParams(window.location.search);
    const langFromUrl = urlParams.get('lang');
    if (langFromUrl && translations[langFromUrl]) {
        return langFromUrl;
    }

    const langFromBrowser = navigator.language.split('-')[0];
    if (translations[langFromBrowser]) {
        return langFromBrowser;
    }

    return 'en'; // Default language
}

// Apply translations on page load
document.addEventListener('DOMContentLoaded', () => {
    const initialLang = getInitialLanguage();
    setLanguage(initialLang);

    // Ensure the dropdown reflects the current language
    const switcher = document.getElementById('languageSwitcher');
    if (switcher) {
        switcher.value = initialLang;
    }
});